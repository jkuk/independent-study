package parser;

public class NonTerminal extends Symbol {
	public NonTerminal(String type) {
		super(type);
	}

	@Override
	public boolean equals(Object object) {
		return super.equals(object);
	}

	@Override
	public int hashCode() {
		return super.hashCode();
	}

	@Override
	public String toString() {
		return super.toString();
	}
}